def suma(a, b):
    """Devuelve la suma de dos números

    Args:
        a (int): Primer número a sumar
        b (int): Segundo número a sumar

    Returns:
        int: Suma de a y b
    """
    return a + b


resultado = suma(a=4, b=10)
print(resultado)

